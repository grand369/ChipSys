<template>
  <el-dialog
    v-model="state.showDialog"
    destroy-on-close
    :title="title"
    append-to-body
    draggable
    :close-on-click-modal="false"
    :close-on-press-escape="false"
    width="780px"
  >
    <div style="padding: 0px 0px 8px 8px; background-color: var(--ba-bg-color)">
      <el-card shadow="never" :body-style="{ paddingBottom: '0' }" style="margin-top: 8px">
        <el-form :model="state.filter" :inline="true" @submit.stop.prevent>
          <el-form-item label="ä¼ä¸šå? prop="name">
            <el-input v-model="state.filter.name" placeholder="ä¼ä¸šå? @keyup.enter="onQuery" />
          </el-form-item>
          <el-form-item>
            <el-button type="primary" icon="ele-Search" @click="onQuery"> æŸ¥è¯¢ </el-button>
          </el-form-item>
        </el-form>
      </el-card>

      <el-card shadow="never" style="margin-top: 8px">
        <el-table
          ref="tenantTableRef"
          :data="state.tenantListData"
          style="width: 100%"
          v-loading="state.loading"
          row-key="id"
          default-expand-all
          :tree-props="{ children: 'children', hasChildren: 'hasChildren' }"
          :highlight-current-row="!multiple"
          @row-click="onRowClick"
          @row-dblclick="onRowDbClick"
        >
          <el-table-column v-if="multiple" type="selection" width="55" />
          <el-table-column prop="name" label="ä¼ä¸šå? min-width="82" show-overflow-tooltip />
          <el-table-column prop="code" label="ä¼ä¸šç¼–ç " min-width="120" show-overflow-tooltip />
          <!-- <el-table-column prop="email" label="é‚®ç®±" min-width="120" show-overflow-tooltip /> -->
        </el-table>
        <div class="my-flex my-flex-end" style="margin-top: 20px">
          <el-pagination
            v-model:currentPage="state.pageInput.currentPage"
            v-model:page-size="state.pageInput.pageSize"
            :total="state.total"
            :page-sizes="[10, 20, 50, 100]"
            background
            @size-change="onSizeChange"
            @current-change="onCurrentChange"
            layout="total, sizes, prev, pager, next"
          />
        </div>
      </el-card>
    </div>
    <template #footer>
      <span class="dialog-footer">
        <el-button @click="onCancel">å?æ¶?/el-button>
        <el-button type="primary" @click="onSure" :loading="sureLoading">ç¡?å®?/el-button>
      </span>
    </template>
  </el-dialog>
</template>

<script lang="ts" setup name="admin/tenant/components/tenant-select">
import { TableInstance } from 'element-plus'
import { TenantGetPageOutput, PageInputTenantGetPageInput } from '/@/api/admin/data-contracts'
import { TenantApi } from '/@/api/admin/Tenant'

const props = defineProps({
  title: {
    type: String,
    default: '',
  },
  multiple: {
    type: Boolean,
    default: false,
  },
  sureLoading: {
    type: Boolean,
    default: false,
  },
})

const emits = defineEmits(['sure'])

const tenantTableRef = useTemplateRef<TableInstance>('tenantTableRef')

const state = reactive({
  showDialog: false,
  loading: false,
  filter: {
    name: '',
  },
  total: 0,
  pageInput: {
    currentPage: 1,
    pageSize: 20,
    filter: {
      name: '',
    },
  } as PageInputTenantGetPageInput,
  tenantListData: [] as Array<TenantGetPageOutput>,
})

// æ‰“å¼€å¯¹è¯æ¡?
const open = () => {
  state.showDialog = true

  onQuery()
}

// å…³é—­å¯¹è¯æ¡?
const close = () => {
  state.showDialog = false
}

const onQuery = async () => {
  state.loading = true
  state.pageInput.filter = state.filter
  const res = await new TenantApi().getPage(state.pageInput).catch(() => {
    state.loading = false
  })

  state.tenantListData = res?.data?.list ?? []
  state.total = res?.data?.total ?? 0
  state.loading = false
}

const onSizeChange = (val: number) => {
  state.pageInput.currentPage = 1
  state.pageInput.pageSize = val
  onQuery()
}

const onCurrentChange = (val: number) => {
  state.pageInput.currentPage = val
  onQuery()
}

const onRowClick = (row: TenantGetPageOutput) => {
  tenantTableRef.value!.toggleRowSelection(row, props.multiple ? undefined : true)
}

const onRowDbClick = () => {
  if (!props.multiple) {
    onSure()
  }
}

// å–æ¶ˆ
const onCancel = () => {
  state.showDialog = false
}

// ç¡®å®š
const onSure = () => {
  const selectionRows = tenantTableRef.value!.getSelectionRows() as TenantGetPageOutput[]
  emits('sure', props.multiple ? selectionRows : selectionRows.length > 0 ? selectionRows[0] : null)
}

defineExpose({
  open,
  close,
})
</script>

<style scoped lang="scss">
:deep(.el-dialog__body) {
  padding: 5px 10px;
}
</style>
