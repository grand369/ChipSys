<template>
  <my-layout>
    <el-card class="my-query-box mt8" shadow="never" :body-style="{ paddingBottom: '0' }">
      <el-form :inline="true" @submit.stop.prevent>
        <el-form-item label="å¹³å°">
          <el-select v-model="state.filter.platform" placeholder="å¹³å°" @change="onQuery" style="width: 100px">
            <el-option v-for="item in state.dictData[DictType.PlatForm.name]" :key="item.code" :label="item.name" :value="item.code" />
          </el-select>
        </el-form-item>
        <el-form-item label="è§†å›¾åç§°">
          <el-input v-model="state.filter.label" placeholder="è§†å›¾åç§°" @keyup.enter="onQuery" />
        </el-form-item>
        <!-- <el-form-item label="è§†å›¾è·¯å¾„">
          <el-input v-model="state.filter.path" placeholder="è§†å›¾è·¯å¾„" @keyup.enter="onQuery" />
        </el-form-item> -->
        <el-form-item>
          <el-button type="primary" icon="ele-Search" @click="onQuery"> æŸ¥è¯¢ </el-button>
          <el-button v-auth="'api:admin:view:add'" type="primary" icon="ele-Plus" @click="onAdd"> æ–°å¢ </el-button>
        </el-form-item>
      </el-form>
    </el-card>

    <el-card class="my-fill mt8" shadow="never">
      <el-table
        :data="state.viewTreeData"
        style="width: 100%"
        v-loading="state.loading"
        row-key="id"
        default-expand-all
        :tree-props="{ children: 'children', hasChildren: 'hasChildren' }"
        border
      >
        <el-table-column prop="label" label="è§†å›¾åç§°" min-width="120" show-overflow-tooltip />
        <el-table-column prop="name" label="è§†å›¾å‘½å" min-width="120" show-overflow-tooltip />
        <el-table-column prop="path" label="è§†å›¾åœ°å€" min-width="120" show-overflow-tooltip />
        <el-table-column prop="sort" label="æ’åº" width="82" align="center" show-overflow-tooltip />
        <!-- <el-table-column prop="description" label="è§†å›¾æè¿°" min-width="120" show-overflow-tooltip /> -->
        <el-table-column label="çŠ¶æ€? width="82" align="center" show-overflow-tooltip>
          <template #default="{ row }">
            <el-tag type="success" v-if="row.enabled">å¯ç”¨</el-tag>
            <el-tag type="danger" v-else>ç¦ç”¨</el-tag>
          </template>
        </el-table-column>
        <el-table-column label="æ“ä½œ" width="210" fixed="right" header-align="center" align="center">
          <template #default="{ row }">
            <el-button v-auth="'api:admin:view:update'" icon="ele-EditPen" text type="primary" @click="onEdit(row)">ç¼–è¾‘</el-button>
            <el-button v-auth="'api:admin:view:delete'" icon="ele-Delete" text type="danger" @click="onDelete(row)">åˆ é™¤</el-button>
            <el-button v-auth="'api:admin:view:add'" icon="ele-CopyDocument" text type="primary" @click="onCopy(row)">å¤åˆ¶</el-button>
          </template>
        </el-table-column>
      </el-table>
    </el-card>

    <view-form ref="viewFormRef" :title="state.viewFormTitle" :view-tree-data="state.viewTreeData"></view-form>
  </my-layout>
